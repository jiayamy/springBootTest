package com.servant.wiki.core.redisDemo.dict.model;

public class Union {

	private Object val;
	
	private Long u64;
	
	private Integer s64;
	
	private Double d;

	public Object getVal() {
		return val;
	}

	public void setVal(Object val) {
		this.val = val;
	}

	public Long getU64() {
		return u64;
	}

	public void setU64(Long u64) {
		this.u64 = u64;
	}

	public Integer getS64() {
		return s64;
	}

	public void setS64(Integer s64) {
		this.s64 = s64;
	}

	public Double getD() {
		return d;
	}

	public void setD(Double d) {
		this.d = d;
	}
	
}
